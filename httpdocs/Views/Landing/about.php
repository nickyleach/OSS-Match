<h2>What is OSS Match</h2>
<p>OSS Match is a tool to help you find open source projects whose code you'll be comfortable working with.</p>

<h2>How does OSS Match work?</h2>
<p>We analyze your code to identify stylistic preferences, habits, patterns, etc. We then compare this data to all the other code we've analyzed, matching you to projects written in the same languages which employ similar patterns.</p>

<h2>Why are you doing this?</h2>
<p>It can be hard finding a good place to get started working with open source projects. </p>